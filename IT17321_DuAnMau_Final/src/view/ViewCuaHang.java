    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        int row = tblSanPham.getSelectedRow();
        SanPham idSP = listCTSP.get(row).getIdSP();
        NXB idNXB = listCTSP.get(row).getIdNXB();
        MauSac idMS = listCTSP.get(row).getIdMauSac();
        DongSP idDSP = listCTSP.get(row).getIdDSP();
        int namBH = listCTSP.get(row).getNamBH();
        String moTa = txtMoTa.getText();
        int soLuong = (int) cboSoLuong.getSelectedItem();
        double giaNhap = Double.valueOf(txtGiaNhap.getText());
        double giaBan = Double.valueOf(txtGiaBan.getText());

        ChiTietSP ctsp = new ChiTietSP(idSP, idNXB, idMS, idDSP, namBH, moTa, soLuong, giaNhap, giaBan);

        JOptionPane.showMessageDialog(this, ctspsi.add(ctsp));
        listCTSP = ctspsi.getAll();
        showDataTable(listCTSP);


    }//GEN-LAST:event_btnAddActionPerformed

