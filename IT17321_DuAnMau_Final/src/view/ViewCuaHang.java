    private void LoadCBBSL() {
        for (int i = 0; i < 1000; i++) {
            listComboxSL.add(i);
        }
        for (Integer i : listComboxSL) {
            dcbmSL.addElement(i);
        }
    }

    private void LoadCBBDSP() {
        for (DongSP dsp : listDSP) {
            dcbmDSP.addElement(dsp.getTenDSP());
        }
    }

    private void LoadCBBNXB() {
        for (NXB nxb : listNXB) {
            dcbmNXB.addElement(nxb.getTenNXB());
        }
    }

    private void LoadCBBSP() {
        for (SanPham sp : listSP) {
            dcbmSP.addElement(sp.getTenSP());
        }
    }

    private void LoadCBBMS() {
        for (MauSac ms : listMS) {
            dcbmMS.addElement(ms.getTenMS());
        }
    }

    private void showDataTable(List<ChiTietSP> list) {
        dtm.setRowCount(0);
        for (ChiTietSP ctsp : list) {
            dtm.addRow(ctsp.toDataRow());
        }
    }

    private void fillData(int index) {
        ChiTietSP ctsp = listCTSP.get(index);
        txtMoTa.setText(ctsp.getMoTa());
        txtGiaBan.setText(String.valueOf(ctsp.getGiaBan()));
        txtGiaNhap.setText(String.valueOf(ctsp.getGiaNhap()));
        cboSoLuong.setSelectedItem(ctsp.getSoLuong());
        cboTenDongSP.setSelectedItem(ctsp.getTenDSP().getTenDSP());
        cboTenMS.setSelectedItem(ctsp.getTenMS().getTenMS());
        cboTenNXB.setSelectedItem(ctsp.getTenNXB().getTenNXB());
        cboTenSP.setSelectedItem(ctsp.getTenSP().getTenSP());
    }

